from optimized_functions import run_me2


def test_run_me():
    assert run_me2([[34, 45, 18, 20, 35, 40, 50, 65, 75]], [45, 46, 47, 48, 52, 60]) == 'S1 3 3 3 3 2 2'
    assert run_me2([[87, 89, 80, 78, 90, 38, 32, 45, 58]], [45, 46, 47, 48, 52, 60]) == 'S1 6 6 6 6 6 5'
    assert run_me2([[34, 45, 18, 20, 35, 40, 50, 65, 75], [87, 89, 80, 78, 90, 38, 32, 45, 58]],
                   [45, 46, 47, 48, 52, 60]) == 'S1 3 3 3 3 2 2\nS2 6 6 6 6 6 5'
    assert run_me2([[1, 1, 1, 1, 1, 1, 1, 1, 1]], [45, 46, 47, 48, 52, 60]) == 'S1 0 0 0 0 0 0'
    assert run_me2([[89, 90, 34, 23, 67, 89, 101, 1045, 89]], [4, 146, 67, 88, 92, 60]) == 'S1 9 7 6 6 2 1'
    assert run_me2([[23, 23, 23, 23, 23, 23, 34, 34, 34]], [4, 146, 67, 88, 92, 60]) == 'S1 9 0 0 0 0 0'
    assert run_me2([[123, 123, 323, 235, 2, 23, 34, 34, 34]], [444, 146, 69, 888, 93, 60]) == 'S1 4 4 4 2 0 0'
